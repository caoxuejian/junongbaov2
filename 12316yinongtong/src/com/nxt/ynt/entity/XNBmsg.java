package com.nxt.ynt.entity;

/*
 * 新农帮消息
 */
import java.io.Serializable;

public class XNBmsg implements Serializable {
	private String id;
	private String upic;
	private String uname;
	private String uid;
	private String tid;
	private String lasttime;
	private String address;
	private String lat;
	private String lng;
	private String lastmsg;
	private String type;
	private String param;
	private String msgnum;
	private String dyflag;
	private String profile;
	private String nick;
	// 订阅号
	private String name;
	private String lid;
	private String image;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLid() {
		return lid;
	}

	public void setLid(String lid) {
		this.lid = lid;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public XNBmsg() {
		super();
	}

	public XNBmsg(String uid, String lasttime, String lastmsg) {
		super();
		this.uid = uid;
		this.lasttime = lasttime;
		this.lastmsg = lastmsg;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}

	public String getDyflag() {
		return dyflag;
	}

	public void setDyflag(String dyflag) {
		this.dyflag = dyflag;
	}

	public String getProfile() {
		return profile;
	}

	public void setProfile(String profile) {
		this.profile = profile;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

	public String getMsgnum() {
		return msgnum;
	}

	public void setMsgnum(String msgnum) {
		this.msgnum = msgnum;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUpic() {
		return upic;
	}

	public void setUpic(String upic) {
		this.upic = upic;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public String getLasttime() {
		return lasttime;
	}

	public void setLasttime(String lasttime) {
		this.lasttime = lasttime;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}

	public String getLastmsg() {
		return lastmsg;
	}

	public void setLastmsg(String lastmsg) {
		this.lastmsg = lastmsg;
	}

	@Override
	public String toString() {
		return "XNBmsg [id=" + id + ", upic=" + upic + ", uname=" + uname
				+ ", uid=" + uid + ", tid=" + tid + ", lasttime=" + lasttime
				+ ", address=" + address + ", lat=" + lat + ", lng=" + lng
				+ ", lastmsg=" + lastmsg + ", type=" + type + ", param="
				+ param + ", msgnum=" + msgnum + ", dyflag=" + dyflag
				+ ", profile=" + profile + ", nick=" + nick + ", name=" + name
				+ ", lid=" + lid + ", image=" + image + "]";
	}

}
